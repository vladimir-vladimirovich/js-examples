// -------------------- SWITCH --------------------
let a = 2;
switch (a) {
    case 0:
        console.log('0 found');
        break;
    case 1:
        console.log('1 found');
        break;
    case 2:
        console.log('2 found');
        break;
    default:
        console.log('Nothing was found');
}
// 2 found

